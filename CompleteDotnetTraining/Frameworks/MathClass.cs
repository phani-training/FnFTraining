using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Frameworks
{
    internal class MathClass
    {
        public double Addfunc(double firstValue, double secondValue) => firstValue + secondValue;

        public double Subfunc(double firstValue, double secondValue) => firstValue - secondValue;

        public double Mulfunc(double firstValue, double secondValue) => firstValue * secondValue;

        public double Divfunc(double firstValue, double secondValue) => firstValue / secondValue;
    }
}
